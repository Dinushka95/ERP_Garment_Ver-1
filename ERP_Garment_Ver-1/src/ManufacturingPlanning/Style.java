    public void fillcombocolor()
    {
        try
        {
            AutoDB_Connect.DB_ResultSet = autoSqlQuery.executeQuery("SELECT DISTINCT Color FROM garmentsystem.T_Design_table ");
            
            while(AutoDB_Connect.DB_ResultSet.next())
            {
                String id = AutoDB_Connect.DB_ResultSet.getString("Color");
                jComboBox4.addItem(id);
                
               
            }
        }
        catch (SQLException ex)
        {
            System.out.println(ex);
        }

    
    }
