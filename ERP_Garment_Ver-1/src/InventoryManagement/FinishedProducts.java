    private void FillCombo()
    {
        try
        {
            AutoDB_Connect.DB_ResultSet = autoSqlQuery.executeQuery("SELECT * FROM garmentsystem.T_Design_table");
            
            while(AutoDB_Connect.DB_ResultSet.next())
            {
                String id = AutoDB_Connect.DB_ResultSet.getString("StyleId");
                ComboDesignID.addItem(id);
            }
        }
        catch (Exception ex)
        {
            System.out.println(ex);
        }
    }
    
    public void AddProduct()
    {
        
       if(validation.ValidationCheck(costfield, true,0,'1')&&validation.ValidationCheck(costfield, true,0,'1')&&validation.ValidationCheck(quantityfield, true,0,'1')&&validation.ValidationCheck(sellingPrice, true,0,'1')){ 
        String id = idfield.getText();
        String name = namefield.getText();
        String designID = ComboDesignID.getSelectedItem().toString();
        String designame = designName.getText();
        String type = typeBox.getSelectedItem().toString();
        String cost =  costfield.getText();
        String qty = quantityfield.getText();
        String value = valuefield.getText();
        String price = sellingPrice.getText();
        String date = datePicker2.getText();
        
        boolean x = autoSqlQuery.execute("INSERT INTO `garmentsystem`.`product_table`\n" +
"(`ProductId`,\n" +
"`ProductName`,\n" +
"`DesignId`,\n" +
"`Design_Description`,\n" +
"`ProductType`,\n" +
"`Cost`,\n" +
"`Quantity`,\n" +
"`Stock_Value`,\n" +
"`Price`,\n" +
"`Date`)\n" +
"VALUES\n" +
"('"+id+"',\n" +
"'"+name+"',\n" +
"'"+designID+"',\n" +
"'"+designame+"',\n" +
"'"+type+"',\n" +
"'"+cost+"',\n" +
"'"+qty+"',\n" +
"'"+value+"',\n" +
"'"+price+"',\n" +
"'"+date+"');");
        
        try
        {
            if(x==true)
            {   autoSqlQuery.executeAutoAddImage(id, autogetimage.getFile());
                TableLoad();
                TextBoxClear();
                TableLoad2();
                clear();
            }
        }
        catch(Exception ex)
        {
            System.out.println("Errr : "+ex);
        }
        
        
        
       }
    }
    
